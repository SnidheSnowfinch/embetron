        <p>While rooftop solar arrays are the most common choice for homeowners, there are many reasons to consider a
            ground mounted solar panel system.</p>
        <p>Generally, ground mount solar panels cost the same as rooftop solar on a per-watt basis. If your property
            isn’t ideal for rooftop solar, installing a ground-mounted system can actually save you money in the long
            term.</p>
        <p>Rooftop solar arrays are restricted by the characteristics of the roof on which they are installed. If your
            roof isn’t at the right angle, doesn’t face south, or has obstructions like chimneys or skylights, then your
            solar array will be less productive. On the other hand, ground mounted solar panels can be located wherever
            the conditions are best.</p>
        <p>In addition, if your home uses a lot of electricity, your roof might not be big enough for a solar energy
            system that meets your electricity needs. In contrast, ground mounted solar systems can be sized to match
            your electricity consumption without the space restrictions of a rooftop system.</p>


        <ul class="off-text">
            <li>
                Not all ground mounted solar panel systems are created equal
            </li>
        </ul>
        <p>There are two basic types of ground mounted solar panel systems:</p>
        <p>Standard ground mounts use metal framing driven into the ground to hold your solar panels up at a fixed
            angle. Some standard ground mounted solar panel systems can be manually adjusted a few times a year to
            account for seasonal shifts of the sun.</p>
        <p>Pole mounts support multiple solar panels on a single pole, and elevate panels higher off the ground than a
            standard ground mount. Pole mounts often incorporate tracking systems, which automatically tilt the solar
            panels to capture the optimal amount of sunshine.</p>
        <p>Tracking systems can increase the production of your solar panels by 25 percent or more. If you decide to add
            a tracking system to your ground mounted solar array, you have the option of either a single-axis or a
            dual-axis system. Single-axis tracking systems move your solar panels over the course of the day to follow
            the sun as it moves through the sky. Dual-axis tracking systems can also adjust based on seasonal variations
            in the sun’s position.</p>
        <ul class="off-text">
            <li>
                Ground mount solar panel systems offer benefits for all homeowners </li>
        </ul>
        <p>Even if you are a good candidate for a rooftop solar energy system, there are many benefits to choosing a
            ground mounted solar panel system.</p>
        <p>First, ground mounted solar panel systems are very easy to place, because they can be located on open land.
            They don’t require drilling into your roof, and the bracings used in standard ground mount systems are
            lightweight and easy to remove. (If you opt for a pole mount, they will usually be installed more securely.)
        </p>
        <p>Second, a ground mount solar panel system can be more productive per panel than a rooftop solar array.
            Because they’re not dependent on your roof, ground mounted solar panels are set at the perfect angle to
            optimize energy production. This means you can generate more electricity than a comparably-sized rooftop
            system, and save more money in the long run.</p>
        <p>Finally, ground mounted solar panels are very easy to access for cleaning and maintenance. If you live in an
            area that receives a lot of snow in the winter, being able to easily sweep the snow off of your solar panels
            is a major convenience.</p>
        <p>Most installers offer a ground mount option, and the cost of standard ground mounted solar is comparable with
            a rooftop solar energy system installation. If you decide to include a tracking system in your installation,
            you will pay an additional upfront cost, but the resulting increase in electricity production can make
            tracking systems cost-effective for many homeowners.</p>
