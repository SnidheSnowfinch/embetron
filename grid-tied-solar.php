    <h1>Advantages of Grid-Tied Systems</h1>
    <ul class="off-text">
        <li>
            Save more money with net metering
        </li>
    </ul>
    <p>A grid-connection will allow you to save more money with solar panels through better efficiency rates, net
        metering, plus lower equipment and installation costs:</p>
    <p>Batteries, and other stand-alone equipment, are required for a fully functional off-grid solar system and add to
        costs as well as maintenance. Grid-tied solar systems are therefore generally cheaper and simpler to install.
    </p>

    <p>Your solar panels will often generate more electricity than what you are capable of consuming. With net metering,
        homeowners can put this excess electricity onto the utility grid instead of storing it themselves with
        batteries.</p>
    <p>Net metering (or feed-in tariff schemes in some countries) play an important role in how solar power is
        incentivized. Without it, residential solar systems would be much less feasible from a financial point of view.
    </p>
    <p>Many utility companies are committed to buying electricity from homeowners at the same rate as they sell it
        themselves.</p>
    <ul class="off-text">
        <li>
            The utility grid is a virtual battery </li>
    </ul>
    <p>Electricity has to be spent in real time. However, it can be temporarily stored as other forms of energy (e.g.
        chemical energy in batteries). Energy storage typically comes with significant losses.</p>
    <p>The electric power grid is in many ways also a battery, without the need for maintenance or replacements, and
        with much better efficiency rates. In other words, more electricity (and more money) goes to waste with
        conventional battery systems.</p>
    <p>According to EIA data[1], national, annual electricity transmission and distribution losses average about 7% of
        the electricity that is transmitted in the United States. Lead-acid batteries, which are commonly used with
        solar panels, are only 80-90% efficient at storing energy, and their performance degrades with time.</p>
    <p>Additional perks of being grid-tied include access to backup power from the utility grid (in case your solar
        system stop generating electricity for one reason or another). At the same time you help to mitigate the utility
        company`s peak load. As a result, the efficiency of our electrical system as a whole goes up.</p>

    <section class=" text-left pt-3">
        <h1>Equipment for Grid-Tied Solar Systems</h1>
        <p>There are a few key differences between the equipment needed for grid-tied, off-grid and hybrid solar
            systems. Standard grid-tied solar systems rely on the following components:</p>
        <ul class="off-text">
            <li>
                Grid-Tie Inverter (GTI) or Micro-Inverters </li>
        </ul>
        <p>
            What is the job of a solar inverter? They regulate the voltage and current received from your solar panels.
            Direct current (DC) from your solar panels is converted into alternating current (AC), which is the type of
            current that is utilized by the majority of electrical appliances.
        </p>
        <p>In addition to this, grid-tie inverters, also known as grid-interactive or synchronous inverters, synchronize
            the phase and frequency of the current to fit the utility grid (nominally 60Hz). The output voltage is also
            adjusted slightly higher than the grid voltage in order for excess electricity to flow outwards to the grid.
        </p>
        <ul class="off-text">
            <li>
                Micro-Inverters
            </li>
        </ul>
        <p>Micro-inverters go on the back of each solar panel, as opposed to one central inverter that typically takes
            on the entire solar array.</p>
        <p>There has recently been a lot of debate on whether micro-inverters are better than central (string) inverters
        </p>
        <p>Micro-inverters are certainly more expensive, but in many cases yield higher efficiency rates. Homeowners who
            are suspect to shading issues should definitely look into if micro-inverters are better in their situation.
        </p>
        <ul class="off-text">
            <li>
                Power Meter </li>
        </ul>
        <p>Most homeowners will need to replace their current power meter with one that is compatible with net metering.
            This device, often called a net meter or a two-way meter, is capable of measuring power going in both
            directions, from the grid to your house and vice versa.</p>
        <p>You should consult with your local utility company and see what net metering options you have. In some
            places, the utility company issues a power meter for free and pay full price for the electricity you
            generate; however, this is not always the case.</p>
