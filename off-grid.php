<section class="work-section text-left pt-0">
    <h1>Advantages of Off-Grid Solar Systems</h1>
    <ul class="off-text">
        <li>
            No access to the utility grid
        </li>
    </ul>

    <p>Off-grid solar systems can be cheaper than extending power lines in certain remote areas. Consider off-gird if
        you’re more than 100 yards from the grid. The costs of overhead transmission lines range from $174,000 per mile
        (for rural construction) to $11,000,000 per mile (for urban construction).
    </p>
    <ul class="off-text">
        <li>
            Become energy self-sufficient </li>
    </ul>
    <p>Living off the grid and being self-sufficient feels good. For some people, this feeling feeling is worth more
        than saving money. Energy self-sufficiency is also a form of security. Power failures on the utility grid do not
        affect off-grid solar systems. On the flip side, batteries can only store a certain amount of energy, and during
        cloudy times, being connected to the grid is actually where the security is. You should install a backup
        generator to be prepared for these kinds of situations.</p>

    <h1>Equipment for Off-Grid Solar Systems</h1>
    <p>Typical off-grid solar systems require the following extra components:</p>
    <ul class="off-text">
        <li>
            Solar Charge Controller </li>
    </ul>

    <p>Solar charge controllers are also known as charge regulators or just battery regulators. The last term is
        probably the best to describe what this device actually does: Solar battery chargers limit the rate of current
        being delivered to the battery bank and protect the batteries from overcharging.</p>
    <p>Good charge controllers are crucial for keeping the batteries healthy, which ensures the lifetime of a battery
        bank is maximized. If you have a battery-based inverter, chances are that the charge controller is integrated.
    </p>
    <ul class="off-text">
        <li>
            Battery Bank </li>
    </ul>

    <p>Without a battery bank (or a generator) it’ll be lights out by sunset. A battery bank is essentially a group of
        batteries wired together.</p>
    <ul class="off-text">
        <li>
            DC Disconnect Switch </li>
    </ul>

    <p>AC and DC safety disconnects are required for all solar systems. For off-grid solar systems, one additional DC
        disconnect is installed between the battery bank and the off-grid inverter. It is used to switch off the current
        flowing between these components. This is important for maintenance, troubleshooting and protection against
        electrical fires.</p>
    <ul class="off-text">
        <li>
            Off-Grid Inverter</li>
    </ul>

    <p>There`s no need for an inverter if you`re only setting up solar panels for your boat, your RV, or something else
        that runs on DC current. You will need an inverter to convert DC to AC for all other electrical appliances.</p>
    <p>Off-grid inverters do not have to match phase with the utility sine wave as opposed to grid-tie inverters.
        Electrical current flows from the solar panels through the solar charge controller and the bank battery bank
        before it is finally converted into AC by the off-grid-inverter.</p>
    <ul class="off-text">
        <li>
            Backup Generator </li>
    </ul>
    <p>It takes a lot of money and big batteries to prepare for several consecutive days without the sun shining (or
        access to the grid). This is where backup generators come in.</p>
    <p>In most cases, installing a backup generator that runs on diesel is a better choice than investing in an
        oversized battery bank that seldom gets to operate at it`s full potential. Generators can run on propane,
        petroleum, gasoline and many other fuel types.</p>
    <p>Backup generators typically output AC, which can be sent through the inverter for direct use, or it can be
        converted into DC for battery storage.</p>
